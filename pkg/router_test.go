func Test_GetURIParts(t *testing.T) {
	for _, tc := range testCasesCurated {
		t.Run(tc.rawUrl, func(t *testing.T) {
			uri, version, tX, err := getURIParts(tc.rawUrl)
			t.Logf("uri: %s, version: %s, type: %v", uri, version, tX)
			assert.Nil(t, err)
			assert.Equal(t, tc.expectedURI, uri)
			assert.Equal(t, tc.expectedVerion, version)
			assert.Equal(t, tc.expectedType, tX)
		})
	}
}

